#include<stdio.h>
main ()
{
	int num1, num2;
	printf("Enter two numbers such that num1 is greater than num2\n");
	scanf("%d %d",&num1,&num2);
	printf("Sum is %d\n",num1+num2);
	printf("Product of two numbers is %d\n",num1*num2);
	printf("Difference of two numbers is %d\n",num1-num2);
	printf("Quotient is %d\n",num1/num2);
	printf("Remainder is %d\n",num1%num2);
	
}
